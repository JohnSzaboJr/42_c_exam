#include <stdio.h>

unsigned int	lcm(unsigned int a, unsigned int b)
{
	unsigned int c;

	c = a;
	if (a == 0 || b == 0)
		return (0);
	while (c % b != 0)
		c = c + a; 
	return (c);
}
