char	*ft_strrev(char *str)
{
	int i;
	int len;
	char a;

	i = 0;
	len = 0;
	a = 'a';
	while (str[len] != '\0')
		len++;
	len--;
	while (i < len)
	{
		a = str[i];
		str[i] = str[len];
		str[len] = a;
		i++;
		len--;
	}
	return (str);
}
